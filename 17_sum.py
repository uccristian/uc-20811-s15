# sum() calcula y devuelve la suma total de todos los elementos numéricos en una lista.
# Solo funciona con listas que contienen números (enteros o decimales).
# Es como usar una calculadora para sumar todos los valores de una columna.

ventas_diarias = [100, 75, 120, 90, 50]
total_ventas = sum(ventas_diarias)
print(total_ventas) # 435

precios = [15.50, 8.25, 20.00, 12.75]
suma_precios = sum(precios)
print(suma_precios) # 56.5