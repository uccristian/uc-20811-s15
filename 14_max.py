# max() encuentra y devuelve el elemento m√°s grande de una lista.
# Al igual que min(), solo funciona si los elementos de la lista se pueden comparar.

temperaturas_diarias = [28, 32, 25, 30, 29]
temp_maxima = max(temperaturas_diarias)
print(temp_maxima) # 32

frutas = ["kiwi", "pera", "manzana", "banana"]
fruta_mayor = max(frutas)
print(fruta_mayor) # pera