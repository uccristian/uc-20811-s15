# append() sirve para añadir un elemento al final de una lista.
# Es como agregar un nuevo artículo al final de tu lista de tareas.

tareas_pendientes = ["comprar leche", "pagar facturas", "llamar al banco"]
print(tareas_pendientes) # ['comprar leche', 'pagar facturas', 'llamar al banco']

tareas_pendientes.append("enviar email")
print(tareas_pendientes) # ['comprar leche', 'pagar facturas', 'llamar al banco', 'enviar email']

numeros_favoritos = [7, 13, 21]
print(numeros_favoritos) # [7, 13, 21]

numeros_favoritos.append(42)
print(numeros_favoritos) # [7, 13, 21, 42]