# min() encuentra y devuelve el elemento más pequeño de una lista.
# Solo funciona con listas cuyos elementos se pueden comparar entre sí (como números o textos).

edades = [45, 18, 62, 33, 29]
edad_minima = min(edades)
print(edad_minima) # 18

palabras = ["zebra", "manzana", "perro", "casa"]
# Encontrar la palabra alfabéticamente menor
palabra_menor = min(palabras)
print(palabra_menor) # casa