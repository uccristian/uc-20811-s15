# insert() añade un elemento en una posición específica de la lista.

tareas = ["revisar email", "hacer informe"]
tareas.insert(1, "llamada al cliente")
print(tareas) # ['revisar email', 'llamada al cliente', 'hacer informe']

numeros = [2, 4, 6]
numeros.insert(0, 0)
print(numeros) # [0, 2, 4, 6]